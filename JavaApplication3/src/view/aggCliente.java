    public void agregar(){
    
    Clientes c = new Clientes();
    LibreriaDAO dao = new LibreriaDAO();
    
     int id=Integer.parseInt(txtIdCliente.getText());
     String nombre = txtNombreCliente.getText();
     String correo = txtCorreoClientee.getText();
     String Telefono = txtTelCliente.getText();
     
    int idLibro = Integer.parseInt(idLibroCliente.getText());
    String nombreLibro = nombreLibroCliente.getText();
    String estado = "Pendiente";
    
    c.setCorreo(correo);
    c.setEstado(estado);
    c.setIdLibro(idLibro);
    c.setIdPersona(id);
    c.setNombreLibroPrestado(nombreLibro);
    c.setNombres(nombre);
    c.setTelefono(Telefono);
    
    
    
    int r=dao.agregarCliente(c);
    
    if(r==1){
        JOptionPane.showMessageDialog(null, "Libro agregado correctamente");
    }else{
        JOptionPane.showMessageDialog(null, "Error");
    }
    
}
    
    
