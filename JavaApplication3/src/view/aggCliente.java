    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int idCli = Integer.parseInt((String) this.txtIdBuscar.getText());
        

        List<Persona> lista = dao.BusquedaId(idCli);
        
        Object[] object = new Object[4];
        for (int i = 0; i < lista.size(); i++) {
            object[0] = lista.get(i).getId();
            object[1] = lista.get(i).getNom();
            object[2] = lista.get(i).getCorreo();
            object[3] = lista.get(i).getTel();
            
   
                
            txtIdCliente.setText(""+object[0]);
            txtNombreCliente.setText(""+object[1]);
            txtCorreoClientee.setText(""+object[2]);
            txtTelCliente.setText(""+object[3]);
          
        
  
}
        
       
       


    }//GEN-LAST:event_btnBuscarActionPerformed

